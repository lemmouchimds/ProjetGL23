    private void btnOKActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnOKActionPerformed
        // verify that the solde field is not empty
        if (tfSolde.getText().isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(null, "Veuillez remplir le solde");
            return;
        }
        // verify that the max field is not empty if the type is 2
        if (jComboBox1.getSelectedItem().equals("2") && tfMax.getText().isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(null, "Veuillez remplir le max");
            return;
        }

        // verify that the solde field is a number
        try {
            Float.parseFloat(tfSolde.getText());
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(null, "Veuillez remplir le solde avec un nombre");
            return;
        }

        // verify that the max field is a number if the type is 2
        if (jComboBox1.getSelectedItem().equals("2")) {
            try {
                Float.parseFloat(tfMax.getText());
            } catch (Exception e) {
                javax.swing.JOptionPane.showMessageDialog(null, "Veuillez remplir le max avec un nombre");
                return;
            }
        }

        // verify that the solde field is positive
        if (Float.parseFloat(tfSolde.getText()) < 0) {
            javax.swing.JOptionPane.showMessageDialog(null, "Veuillez remplir le solde avec un nombre positif");
            return;
        }

        // verify that the max field is positive if the type is 2
        if (jComboBox1.getSelectedItem().equals("2") && Float.parseFloat(tfMax.getText()) < 0) {
            javax.swing.JOptionPane.showMessageDialog(null, "Veuillez remplir le max avec un nombre positif");
            return;
        }

        // insert in the database
        CompteDto c = new CompteDto(0, idClient, new java.sql.Date(new java.util.Date().getTime()),
                Float.parseFloat(tfSolde.getText()),
                Integer.parseInt(jComboBox1.getSelectedItem().toString()),
                jComboBox1.getSelectedItem().equals("2") ? Float.parseFloat(tfMax.getText()) : 0);
        db.insertCompte(c);
        //return to the previous screen
        dispose();


    }// GEN-LAST:event_btnOKActionPerformed

