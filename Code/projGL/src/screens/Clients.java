    private void refresh() {
        int index = cbAffichage.getSelectedIndex();

        switch (index) {
            case 0:
                afficherClientsPhysic();
                break;
            case 1:
                afficherClientsMoral();
                break;
            default:
                break;
        }

    }

    private void afficherClientsPhysic() {
        modelPhy.setRowCount(0);
        modelMor.setRowCount(0);

        var list = dbc.findAllClient();
        for (var c : list) {
            if (c.isPhysic) {
                modelPhy.addRow(new Object[] { c.getIdClient(), c.getNom(), c.getPrenom(), c.getDate(), c.getAddress(),
                        c.getTel(), c.getEmail(), c.getProfession() });
            }

        }
    }

    private void afficherClientsMoral() {
        modelPhy.setRowCount(0);
        modelMor.setRowCount(0);
        var list = dbc.findAllClient();
        for (var c : list) {
            if (!c.isPhysic) {
                modelPhy.addRow(new Object[] { c.getIdClient(), c.getNom(), c.getPrenom(), c.getDate(), c.getAddress(),
                        c.getTel(), c.getEmail(), c.getProfession() });
                modelMor.addRow(new Object[] { c.getDesignation(), c.getActiviter(), c.getNrc(),
                        c.getNif() });
            }

        }

    }

    private int selectedID()
    {
        //verify that exactly one row is selected
        int row = tableClientsPhysic.getSelectedRow();        
        if (row == -1 || tableClientsPhysic.getSelectedRowCount() != 1) {
            //show error message to the user in a dialog
            javax.swing.JOptionPane.showMessageDialog(this, "Please select exactly one row to delete!");
            return -1;
        }

        //get the selected row's id
        return (int) tableClientsPhysic.getValueAt(row, 0);
    }

