    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnDeleteActionPerformed
        int id = selectedID();
        if (id == -1) {
            return;
        }

        //ask the user to confirm deletion
        int response = javax.swing.JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this row?",
                "Confirm", javax.swing.JOptionPane.YES_NO_OPTION, javax.swing.JOptionPane.QUESTION_MESSAGE);

        //if the user confirmed deletion
        if (response == javax.swing.JOptionPane.YES_OPTION) {
            //delete the row from the database
            dbc.delete(id, tableType.client);
            //refresh the table
            refresh();
        }


    }// GEN-LAST:event_btnDeleteActionPerformed

